import { createClient } from '@/lib/supabase-server'
import SidebarClient from './SidebarClient'

export const metadata = {
  title: 'Dashboard — RepSignal',
}

export default async function AppLayout({
  children,
}: {
  children: React.ReactNode
}) {
  const supabase = createClient()
  const {
    data: { user },
  } = await supabase.auth.getUser()

  // No user — render without sidebar (login/signup pages)
  if (!user) {
    return <>{children}</>
  }

  return (
    <div className="flex h-screen overflow-hidden bg-[#f4f8fc]">
      {/* Sidebar */}
      <aside className="flex w-64 flex-shrink-0 flex-col bg-[#0f3161] text-white">
        {/* Logo */}
        <div className="flex items-center gap-3 border-b border-white/10 px-6 py-5">
          <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-[#46a095]">
            <svg
              xmlns="http://www.w3.org/2000/svg"
              viewBox="0 0 24 24"
              fill="none"
              stroke="white"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
              className="h-4 w-4"
            >
              <path d="M12 2L9.5 9.5 2 12l7.5 2.5L12 22l2.5-7.5L22 12l-7.5-2.5L12 2z" />
            </svg>
          </div>
          <span className="text-base font-bold tracking-tight">RepSignal</span>
        </div>

        {/* Nav */}
        <nav className="flex-1 space-y-1 px-3 py-4">
          <NavLink href="/app/dashboard" icon={<DashboardIcon />} label="Dashboard" />
          <NavLink href="/app/reviews" icon={<ReviewsIcon />} label="Reviews" />
          <NavLink href="/app/settings" icon={<SettingsIcon />} label="Settings" />
        </nav>

        {/* User + Logout */}
        <SidebarClient email={user.email ?? ''} />
      </aside>

      {/* Main content */}
      <main className="flex flex-1 flex-col overflow-y-auto">
        {children}
      </main>
    </div>
  )
}

function NavLink({
  href,
  icon,
  label,
}: {
  href: string
  icon: React.ReactNode
  label: string
}) {
  return (
    <a
      href={href}
      className="flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium text-white/70 transition-all duration-150 hover:bg-white/10 hover:text-white"
    >
      <span className="h-5 w-5 flex-shrink-0">{icon}</span>
      {label}
    </a>
  )
}

